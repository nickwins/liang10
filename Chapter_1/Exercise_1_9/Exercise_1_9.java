public class Exercise_1_9 {
	public static void main(String[] args) {
		// output area calculation
		System.out.println("Area: " + (4.5 * 7.9));
		
		// output perimeter calculation
		System.out.println("Perimeter: " + (4.5 + 7.9 + 4.5 + 7.9));
	}
}
